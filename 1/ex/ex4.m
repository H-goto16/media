t = 0:1/32000:1;                        % 1秒を1/32000に分割
a = 0.8;                                % 振幅

Y = 2 * a * cos(2 * pi() * (442 - 438) / 2 * 2) ...    % うなりの波形作成
    .* sin(2 * pi() * (442 + 438) / 2 * t);    

plot(t, Y, "c-")     % 正弦波をプロット
xlabel('time','FontSize',12);           % X軸ラベルの設定
xlim([0 0.01]);                         % X軸ラベルの範囲
ylabel('amplitude','FontSize',12);      % Y軸ラベルの設定
ylim([-2.0 2.0]);                       % Y軸ラベルの範囲

